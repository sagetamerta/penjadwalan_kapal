package Penyebrangan_Kapal;

public class Kromosom {
	String kode;
	String kr;
	
	public Kromosom(String kode) {
		super();
		this.kode = kode;
	}

	public String getKode() {
		return kode;
	}

	public void setKode(String kode) {
		this.kode = kode;
	}

	public String getKr() {
		return kr;
	}

	public void setKr(String kr) {
		this.kr = kr;
	}
	
	
}
